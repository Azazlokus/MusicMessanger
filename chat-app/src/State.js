// Класс состояния "Страница"
class PageState {
    constructor() {
        this.state = 'empty'; // Изначальное состояние - пустая страница
    }

    // Метод для установки нового состояния
    setState(newState) {
        this.state = newState;
        this.renderState();
    }

    // Метод для отображения текущего состояния в консоль
    renderState() {
        console.log(`Текущее состояние страницы: ${this.state}`);
    }
}

// Создание экземпляра класса состояния "Страница"
const page = new PageState();

// Использование состояний
page.setState('загрузка'); // Текущее состояние страницы: загрузка
page.setState('заполнена'); // Текущее состояние страницы: заполнена
page.setState('ошибка'); // Текущее состояние страницы: ошибка